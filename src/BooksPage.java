    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       int selectedRow = jTable1.getSelectedRow();
       if(selectedRow == -1){
           JOptionPane.showMessageDialog(this, "Please select a book to delete","Selection Error",JOptionPane.WARNING_MESSAGE);
           return;
       }
       String bookId = jTable1.getValueAt(selectedRow, 0).toString();
       int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this book?","Confirm Deletion",JOptionPane.YES_NO_OPTION);
       if(confirm == JOptionPane.YES_OPTION){
           try{
               con1 = DriverManager.getConnection("jdbc:mysql://localhost/librarydb", "root","");
               insert =con1.prepareStatement("DELETE FROM books where book_id = ?");
               insert.setString(1, bookId);
               int deletedRows = insert.executeUpdate();
               if(deletedRows > 0){
                   JOptionPane.showMessageDialog(this, "Deleted Sucessfully!");
                   update_table();
                   
               }else{
                   JOptionPane.showMessageDialog(this, "Failed to delete the book","Error",JOptionPane.ERROR_MESSAGE);
                   
               }
               con1.close();
           }catch (SQLException ex) {
            Logger.getLogger(BooksPage.class.getName()).log(Level.SEVERE, null, ex);
        }
       }
    }//GEN-LAST:event_jButton3ActionPerformed

