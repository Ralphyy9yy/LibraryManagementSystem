    public void update_table(){
         int c;
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con1 = DriverManager.getConnection("jdbc:mysql://localhost/librarydb","root","");
            insert = con1.prepareStatement("select * from books");
           ResultSet rs = insert.executeQuery();
          ResultSetMetaData rss = rs.getMetaData();
          c = rss.getColumnCount();
          
          
          DefaultTableModel df = (DefaultTableModel)jTable1.getModel();
          df.setRowCount(0);
          while(rs.next()){
                Vector vector = new Vector();
             for (int a = 1; a <= c; a++) {
                vector.add(rs.getString(a)); 
            }
              df.addRow(vector);
          }
          
          
            
            
           con1.close();
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(BooksPage.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

